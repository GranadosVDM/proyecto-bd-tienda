-- 4. FUNCIONES
-- ============================================
CREATE OR REPLACE FUNCTION FN_TOTAL_PEDIDO (P_ID_PEDIDO IN NUMBER) RETURN NUMBER AS V_TOTAL PEDIDO.TOTAL %TYPE;
BEGIN
SELECT TOTAL INTO V_TOTAL
FROM PEDIDO
WHERE ID_PEDIDO = P_ID_PEDIDO;
IF V_TOTAL IS NULL THEN RAISE_APPLICATION_ERROR(-20040, 'El pedido no tiene total calculado.');
END IF;
RETURN V_TOTAL;
EXCEPTION
WHEN NO_DATA_FOUND THEN RAISE_APPLICATION_ERROR(-20041, 'El pedido no existe.');
END;
/
CREATE OR REPLACE FUNCTION FN_CANTIDAD_FACTURAS_CLIENTE (P_ID_CLIENTE IN NUMBER) RETURN NUMBER AS V_CANTIDAD NUMBER;
BEGIN
SELECT COUNT(*) INTO V_CANTIDAD
FROM FACTURA F
    JOIN PEDIDO P ON F.ID_PEDIDO = P.ID_PEDIDO
WHERE P.ID_CLIENTE = P_ID_CLIENTE;
RETURN V_CANTIDAD;
END;
/ -- ============================================
-- 5. CURSORES
-- ============================================
CREATE OR REPLACE PROCEDURE MOSTRAR_FACTURAS_CLIENTE (P_ID_CLIENTE IN NUMBER) AS CURSOR C_FACTURAS IS
SELECT F.ID_FACTURA,
    F.ID_PEDIDO,
    F.FECHA,
    F.TOTAL
FROM FACTURA F
    JOIN PEDIDO P ON F.ID_PEDIDO = P.ID_PEDIDO
WHERE P.ID_CLIENTE = P_ID_CLIENTE
ORDER BY F.ID_FACTURA;
V_CONTADOR NUMBER := 0;
BEGIN FOR R IN C_FACTURAS LOOP V_CONTADOR := V_CONTADOR + 1;
DBMS_OUTPUT.PUT_LINE(
    'FACTURA: ' || R.ID_FACTURA || ' | PEDIDO: ' || R.ID_PEDIDO || ' | FECHA: ' || TO_CHAR(R.FECHA, 'DD/MM/YY') || ' | TOTAL: ' || R.TOTAL
);
END LOOP;
IF V_CONTADOR = 0 THEN DBMS_OUTPUT.PUT_LINE(
    'No hay facturas para el cliente ' || P_ID_CLIENTE
);
END IF;
END;
/
CREATE OR REPLACE PROCEDURE MOSTRAR_INVENTARIO_CURSOR AS CURSOR C_INVENTARIO IS
SELECT ID_PRODUCTO,
    NOMBRE,
    STOCK,
    PRECIO
FROM PRODUCTO
ORDER BY ID_PRODUCTO;
BEGIN FOR R IN C_INVENTARIO LOOP DBMS_OUTPUT.PUT_LINE(
    'PRODUCTO: ' || R.ID_PRODUCTO || ' | NOMBRE: ' || R.NOMBRE || ' | STOCK: ' || R.STOCK || ' | PRECIO: ' || R.PRECIO
);
END LOOP;
END;
