-- 3. PROCEDIMIENTOS
-- ============================================
CREATE OR REPLACE PROCEDURE INSERTAR_PEDIDO (P_ID_CLIENTE IN NUMBER) AS BEGIN
INSERT INTO PEDIDO (FECHA, ID_CLIENTE, TOTAL)
VALUES (SYSDATE, P_ID_CLIENTE, NULL);
END;
/
CREATE OR REPLACE PROCEDURE INSERTAR_DETALLE (
        P_ID_PEDIDO IN NUMBER,
        P_ID_PRODUCTO IN NUMBER,
        P_CANTIDAD IN NUMBER
    ) AS V_PRECIO PRODUCTO.PRECIO %TYPE;
V_STOCK PRODUCTO.STOCK %TYPE;
BEGIN
SELECT PRECIO,
    STOCK INTO V_PRECIO,
    V_STOCK
FROM PRODUCTO
WHERE ID_PRODUCTO = P_ID_PRODUCTO;
IF V_STOCK < P_CANTIDAD THEN RAISE_APPLICATION_ERROR(-20010, 'Stock insuficiente para el producto.');
END IF;
INSERT INTO DETALLE_PEDIDO (
        ID_PEDIDO,
        ID_PRODUCTO,
        CANTIDAD,
        PRECIO_UNITARIO,
        SUBTOTAL
    )
VALUES (
        P_ID_PEDIDO,
        P_ID_PRODUCTO,
        P_CANTIDAD,
        V_PRECIO,
        V_PRECIO * P_CANTIDAD
    );
EXCEPTION
WHEN NO_DATA_FOUND THEN RAISE_APPLICATION_ERROR(-20011, 'El producto no existe.');
END;
/
CREATE OR REPLACE PROCEDURE ACTUALIZAR_TOTAL_PEDIDO (P_ID_PEDIDO IN NUMBER) AS V_TOTAL NUMBER(10, 2);
BEGIN
SELECT NVL(SUM(SUBTOTAL), 0) INTO V_TOTAL
FROM DETALLE_PEDIDO
WHERE ID_PEDIDO = P_ID_PEDIDO;
UPDATE PEDIDO
SET TOTAL = V_TOTAL
WHERE ID_PEDIDO = P_ID_PEDIDO;
END;
/
CREATE OR REPLACE PROCEDURE DESCONTAR_STOCK (
        P_ID_PRODUCTO IN NUMBER,
        P_CANTIDAD IN NUMBER
    ) AS V_STOCK PRODUCTO.STOCK %TYPE;
BEGIN
SELECT STOCK INTO V_STOCK
FROM PRODUCTO
WHERE ID_PRODUCTO = P_ID_PRODUCTO;
IF V_STOCK < P_CANTIDAD THEN RAISE_APPLICATION_ERROR(-20012, 'Stock insuficiente para descontar.');
END IF;
UPDATE PRODUCTO
SET STOCK = STOCK - P_CANTIDAD
WHERE ID_PRODUCTO = P_ID_PRODUCTO;
EXCEPTION
WHEN NO_DATA_FOUND THEN RAISE_APPLICATION_ERROR(-20013, 'El producto no existe.');
END;
/
CREATE OR REPLACE PROCEDURE AUMENTAR_STOCK (
        P_ID_PRODUCTO IN NUMBER,
        P_CANTIDAD IN NUMBER
    ) AS BEGIN
UPDATE PRODUCTO
SET STOCK = STOCK + P_CANTIDAD
WHERE ID_PRODUCTO = P_ID_PRODUCTO;
IF SQL %ROWCOUNT = 0 THEN RAISE_APPLICATION_ERROR(-20014, 'El producto no existe.');
END IF;
END;
/
CREATE OR REPLACE PROCEDURE REGISTRAR_DETALLE_VENTA (
        P_ID_PEDIDO IN NUMBER,
        P_ID_PRODUCTO IN NUMBER,
        P_CANTIDAD IN NUMBER
    ) AS BEGIN INSERTAR_DETALLE(P_ID_PEDIDO, P_ID_PRODUCTO, P_CANTIDAD);
DESCONTAR_STOCK(P_ID_PRODUCTO, P_CANTIDAD);
ACTUALIZAR_TOTAL_PEDIDO(P_ID_PEDIDO);
END;
/
CREATE OR REPLACE PROCEDURE ELIMINAR_PEDIDO_VACIO (P_ID_PEDIDO IN NUMBER) AS V_CANTIDAD NUMBER;
BEGIN
SELECT COUNT(*) INTO V_CANTIDAD
FROM DETALLE_PEDIDO
WHERE ID_PEDIDO = P_ID_PEDIDO;
IF V_CANTIDAD = 0 THEN
DELETE FROM PEDIDO
WHERE ID_PEDIDO = P_ID_PEDIDO;
ELSE RAISE_APPLICATION_ERROR(
    -20020,
    'El pedido tiene detalles y no puede eliminarse.'
);
END IF;
END;
/
CREATE OR REPLACE PROCEDURE INSERTAR_FACTURA (P_ID_PEDIDO IN NUMBER) AS V_FECHA PEDIDO.FECHA %TYPE;
V_TOTAL PEDIDO.TOTAL %TYPE;
BEGIN
SELECT FECHA,
    TOTAL INTO V_FECHA,
    V_TOTAL
FROM PEDIDO
WHERE ID_PEDIDO = P_ID_PEDIDO;
IF V_TOTAL IS NULL THEN RAISE_APPLICATION_ERROR(-20031, 'El pedido no tiene total calculado.');
END IF;
INSERT INTO FACTURA (ID_PEDIDO, FECHA, TOTAL)
VALUES (P_ID_PEDIDO, V_FECHA, V_TOTAL);
EXCEPTION
WHEN NO_DATA_FOUND THEN RAISE_APPLICATION_ERROR(-20030, 'El pedido no existe.');
WHEN DUP_VAL_ON_INDEX THEN RAISE_APPLICATION_ERROR(
    -20032,
    'El pedido ya tiene una factura registrada.'
);
END;
