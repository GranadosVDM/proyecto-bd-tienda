-- 1. CREACION DE TABLAS
-- ============================================
CREATE TABLE CATEGORIA (
    ID_CATEGORIA NUMBER NOT NULL,
    NOMBRE VARCHAR2(100),
    DESCRIPCION VARCHAR2(200),
    ESTADO VARCHAR2(10) NOT NULL,
    CONSTRAINT PK_CATEGORIA PRIMARY KEY (ID_CATEGORIA),
    CONSTRAINT UQ_CATEGORIA_NOMBRE UNIQUE (NOMBRE),
    CONSTRAINT CK_CATEGORIA_ESTADO CHECK (ESTADO IN ('ACTIVA', 'INACTIVA'))
);
CREATE TABLE CLIENTE (
    ID_CLIENTE NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100) NOT NULL,
    CORREO VARCHAR2(100) NOT NULL,
    TELEFONO VARCHAR2(20),
    CONSTRAINT PK_CLIENTE PRIMARY KEY (ID_CLIENTE),
    CONSTRAINT UQ_CLIENTE_CORREO UNIQUE (CORREO)
);
CREATE TABLE PRODUCTO (
    ID_PRODUCTO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100) NOT NULL,
    PRECIO NUMBER(10, 2) NOT NULL,
    STOCK NUMBER NOT NULL,
    ID_CATEGORIA NUMBER,
    CONSTRAINT PK_PRODUCTO PRIMARY KEY (ID_PRODUCTO),
    CONSTRAINT FK_PRODUCTO_CATEGORIA FOREIGN KEY (ID_CATEGORIA) REFERENCES CATEGORIA(ID_CATEGORIA),
    CONSTRAINT CK_PRODUCTO_PRECIO CHECK (PRECIO >= 0),
    CONSTRAINT CK_PRODUCTO_STOCK CHECK (STOCK >= 0)
);
CREATE TABLE PEDIDO (
    ID_PEDIDO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    FECHA DATE DEFAULT SYSDATE NOT NULL,
    ID_CLIENTE NUMBER NOT NULL,
    TOTAL NUMBER(10, 2),
    CONSTRAINT PK_PEDIDO PRIMARY KEY (ID_PEDIDO),
    CONSTRAINT FK_PEDIDO_CLIENTE FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE(ID_CLIENTE)
);
CREATE TABLE DETALLE_PEDIDO (
    ID_DETALLE NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_PEDIDO NUMBER NOT NULL,
    ID_PRODUCTO NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL,
    PRECIO_UNITARIO NUMBER(10, 2) NOT NULL,
    SUBTOTAL NUMBER(10, 2) NOT NULL,
    CONSTRAINT PK_DETALLE_PEDIDO PRIMARY KEY (ID_DETALLE),
    CONSTRAINT FK_DETALLE_PEDIDO_PEDIDO FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDO(ID_PEDIDO),
    CONSTRAINT FK_DETALLE_PEDIDO_PRODUCTO FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO),
    CONSTRAINT CK_DETALLE_CANTIDAD CHECK (CANTIDAD > 0),
    CONSTRAINT CK_DETALLE_PRECIO CHECK (PRECIO_UNITARIO >= 0),
    CONSTRAINT CK_DETALLE_SUBTOTAL CHECK (SUBTOTAL >= 0)
);
CREATE TABLE FACTURA (
    ID_FACTURA NUMBER NOT NULL,
    ID_PEDIDO NUMBER NOT NULL,
    FECHA DATE NOT NULL,
    TOTAL NUMBER(10, 2) NOT NULL,
    CONSTRAINT PK_FACTURA PRIMARY KEY (ID_FACTURA),
    CONSTRAINT FK_FACTURA_PEDIDO FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDO(ID_PEDIDO),
    CONSTRAINT UQ_FACTURA_ID_PEDIDO UNIQUE (ID_PEDIDO)
);
CREATE TABLE HISTORICO_PRECIOS (
    ID_HISTORICO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_PRODUCTO NUMBER NOT NULL,
    PRECIO_ANTERIOR NUMBER(10, 2) NOT NULL,
    PRECIO_NUEVO NUMBER(10, 2) NOT NULL,
    FECHA_CAMBIO DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT PK_HISTORICO_PRECIOS PRIMARY KEY (ID_HISTORICO),
    CONSTRAINT FK_HISTORICO_PRODUCTO FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO)
);
-- ============================================
-- 2. SECUENCIAS Y TRIGGERS
-- ============================================
CREATE SEQUENCE SEQ_CATEGORIA START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE OR REPLACE TRIGGER TRG_CATEGORIA_BI BEFORE
INSERT ON CATEGORIA FOR EACH ROW BEGIN IF :NEW.ID_CATEGORIA IS NULL THEN
SELECT SEQ_CATEGORIA.NEXTVAL INTO :NEW.ID_CATEGORIA
FROM DUAL;
END IF;
END;
/ CREATE SEQUENCE SEQ_FACTURA START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE OR REPLACE TRIGGER TRG_FACTURA_BI BEFORE
INSERT ON FACTURA FOR EACH ROW BEGIN IF :NEW.ID_FACTURA IS NULL THEN
SELECT SEQ_FACTURA.NEXTVAL INTO :NEW.ID_FACTURA
FROM DUAL;
END IF;
END;
